from scipy.optimize import linprog

# Коэффициенты целевой функции (минимизируем отрицательную прибыль)
c = [-900, -500, -700]

# Коэффициенты ограничений (левые части)
A = [
    [0.2, 0.3, 0.32],  # Кожа
    [0.5, 0.2, 0.45],  # Каблуки
    [0.027, 0.022, 0.21],  # Стельки
    [0.1, 0.25, 0.28],  # Подошвы
]
b = [2700, 800, 1600, 100]  # Правые части

# Границы переменных (x >= 0)
x_bounds = (0, None)

# Решение задачи
result = linprog(c, A_ub=A, b_ub=b, bounds=[x_bounds, x_bounds, x_bounds], method="highs")

# Вывод результатов
print("Оптимальные количества:")
print(f"Сапоги: {result.x[0]:.2f}")
print(f"Ботинки: {result.x[1]:.2f}")
print(f"Ботильоны: {result.x[2]:.2f}")
print(f"Максимальная прибыль: {-result.fun:.2f} руб.")
